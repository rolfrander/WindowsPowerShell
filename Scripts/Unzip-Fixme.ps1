$zipPackage = $shellApplication.NameSpace($zipfile)

$destination = Get-Item .
$destinationFolder = $shellApplication.NameSpace($destination.FullName)

$zipPackage.items() | % {
    $destinationFolder.CopyHere($_)
}
    
